public enum Shape {
    /**
     * enum of the types of cards
     */
    Clubs {
        public String toString() {
            return "♠";
        }
    },
    Diamonds {
        public String toString() {
            return "♦";
        }
    },
    Spades {
        public String toString() {
            return "♣";
        }
    },
    Hearts {
        public String toString() {
            return "♥";
        }
    }
}
